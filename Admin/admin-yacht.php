    <script>
        // Initial filters data
        const filters = [
            { id: 1, label: "SD" },
            { id: 2, label: "ANTIFOULING" },
            { id: 3, label: "ANODES" },
            { id: 4, label: "40, SS, STAINLESS (FITTING) INSTALLATION" },
        ];

        // Get DOM elements
        const filterTagsContainer = document.getElementById('filterTags');
        const clearFiltersBtn = document.getElementById('clearFilters');

        // Render filter tags
        function renderFilterTags() {
            filterTagsContainer.innerHTML = filters.map(filter => `
                <span class="filter-tag">
                    ${filter.label}
                    <button onclick="removeFilter(${filter.id})" aria-label="Remove ${filter.label} filter">×</button>
                </span>
            `).join('');
        }

        // Remove filter function
        function removeFilter(id) {
            const index = filters.findIndex(filter => filter.id === id);
            if (index !== -1) {
                filters.splice(index, 1);
                renderFilterTags();
            }
        }

        // Clear all filters
        clearFiltersBtn.addEventListener('click', () => {
            filters.length = 0;
            renderFilterTags();
        });

        // Initial render
        renderFilterTags();
    </script>

